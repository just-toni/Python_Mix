class Calculator(object):

    @classmethod
    def add(cls, param, param1):
        return param + param1

    @classmethod
    def subtract(cls, param, param1):
        return param - param1

    @classmethod
    def multiply(cls, param, param1):
        return param * param1

    @classmethod
    def divide(cls, param, param1):
        return param // param1
